package Quiz2;

public class Lec3 {

	public static void main(String[] args) {
		// TODO play with operators
		int data1=25;
		int data2=12;
		//Bitwise Or
		System.out.println(data1 | data2);
		
		//Bitwise complement
		System.out.println(~35);
		
		// XOR
		System.out.println(12^25);
		
		//Right Shift
		System.out.println(20>>4);
		
		//Zero fill right Shift
		System.out.println(20>>>4);
		
		//Ternary Operators
		System.out.println((10>3)? 3:2);

	}

}
